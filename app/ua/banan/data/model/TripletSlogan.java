package ua.banan.data.model;

public class TripletSlogan
{
	private String slogan0;
	private String slogan1;
	private String slogan2;
		
	public TripletSlogan(String slogan0, String slogan1, String slogan2)
	{
		this.slogan0 = slogan0;
		this.slogan1 = slogan1;
		this.slogan2 = slogan2;
	}
	
	public String getSlogan0()
	{
		return slogan0;
	}
	public void setSlogan0(String slogan0)
	{
		this.slogan0 = slogan0;
	}
	public String getSlogan1()
	{
		return slogan1;
	}
	public void setSlogan1(String slogan1)
	{
		this.slogan1 = slogan1;
	}
	public String getSlogan2()
	{
		return slogan2;
	}
	public void setSlogan2(String slogan2)
	{
		this.slogan2 = slogan2;
	}
	
	
}
